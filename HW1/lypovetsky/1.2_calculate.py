# define vars
print("Give me a number 'a'")
a = int(input())

print(end='\n')  # just to make it prettier

print("Give me a number 'b'")
b = int(input())

# print
print(end='\n')  # just to make it prettier

print('a =', a, '\n' + 'b =', b, end='\n\n')
print('a + b:', a + b)
print('a - b:', a - b)
print('a * b:', a * b)
print('a / b:', a / b)
print('a**b:', a**b)
